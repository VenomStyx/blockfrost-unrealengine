// GET PROJECT KEY BY NAME
////////////////////////////////////////
FString Ublockfrost_settings::GetProjectKeyByName(FName ProjectName)
{
    // Look for project key
    if (ProjectKeys.Contains(ProjectName))
    {
        return ProjectKeys[ProjectName];
    }
    else
    {

        // Log
        UE_LOG(LogTemp, Error, TEXT("blockfrost: Project key not found for project name: %s"), *ProjectName.ToString());

        // If not found, return empty string
        FString EmptyString = "";
        return EmptyString;
        
    }
}